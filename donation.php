<h3>Doneren</h3>
<p>Stichting Hospice Amsterdam Zuidoost is afhankelijk van donateurs en giften van particulieren en bedrijven.
Ook u kunt een belangrijke bijdrage leveren aan de Stichting Hospice Amsterdam Zuidoost door ons financieel te steunen.
</p>
<p>U kunt uw gift overmaken op bankrekeningnummer  NL 27 INGB 0007894849 t.n.v. Stichting Hospice Amsterdam Zuidoost.
</p>
<p>Wij stellen uw donatie zeer op prijs.</p>

<p>Stichting Hospice Amsterdam Zuidoost heeft een ANBI status (onder fiscaal nummer 857859924)</p>

<p>Door het verkrijgen van de ANBI-status zijn periodieke giften aan het hospice geheel aftrekbaar bij de belastingdienst. Eenmalige giften zijn aftrekbaar, afhankelijk van uw inkomen. Via onderstaande link komt u bij het formulier om een periodieke gift te verwezenlijken.</p>

<a href="https://download.belastingdienst.nl/belastingdienst/docs/overeenkomst_%20period_gift_in_geld_ib0802z2fol.pdf">Overeenkomost voor periodieke gift</a>
</div>
