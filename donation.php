  <h3>Bedrijven en sponsoring</h3>
  <p>Naast verschillende fondsen hebben ook bedrijven een bijdrage geleverd aan het ontstaan van het Hospice. Zo willen wij Triatact bedanken voor hun steun bij het vinden van een geschikte pand en hun ondersteuning bij de verbouwing hiervan. Bruynzeel keukens bedanken voor hun bijdrage aan de keuken van het Hospice en ATAG voor het leveren van keukenapparatuur. Daarnaast helpt Decokay ons met de raambekleding van de woonkamer van het Hospice. Ook willen wij Averesch BV bedanken voor de bouwmaterialen die zij gedoneerd hebben voor het realiseren van de badkamers.</p>
<p>Tot slot willen wij Pantar bedanken voor het werk dat zij hebben verricht in onze tuin. Met deze hulp is onze tuin goed schoongemaakt en ontdaan van afval wat in de afgelopen jaren verzameld is toen het pand leeg stond. Dit is een stap in de goede richting zodat wij de bewoners en bezoek een mooi terras kunnen bieden om te genieten van de buitenlucht.</p>
